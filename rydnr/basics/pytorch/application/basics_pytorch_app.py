import asyncio
from pythoneda.shared.application import PythonEDA


class BasicsPytorchApp(PythonEDA):
    """
    Runs the BasicsPytorch PythonEDA app.

    Class name: BasicsPytorchApp

    Responsibilities:
        - Runs the Basics-Pytorch application.

    Collaborators:
        - Command-line handlers from rydnr.basics.pytorch.infrastructure.cli
    """

    def __init__(self):
        """
        Creates a new BasicsPytorchApp instance.
        """
        # basics_pytorch_banner is automatically generated by rydnr/basics-pytorch/nix's flake.
        try:
            from rydnr.basics.pytorch.application.basics_pytorch_banner import (
                BasicsPytorchBanner,
            )

            banner = BasicsPytorchBanner()
        except ImportError:
            banner = None

        super().__init__(banner, __file__)


if __name__ == "__main__":
    asyncio.run(
        BasicsPytorchApp.main("rydnr.basics.pytorch.application.BasicsPytorchApp")
    )

